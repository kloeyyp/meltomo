							<p><h2> 2. Nihongo Challenge <br>
                      Grammar & Reading by Mariko Yamabe </h2>
              <p>
								   <h5> Summary: Specifically for Japanese learners of the N4 level.</h5>

						 <p> Rated as 5 out of 5 on <a href="http://www.amazon.co.uk/charenji-challenge-practice-Grama%C3%8Ctica-exerci%C3%8Ccios/dp/4872177568">Amazon</a> but the rating may not be reliable since there are only 2 reviewers.


							<p>
                   <b>Pros: </b>
                    <br>•	Easy to use for those who wants to self-study.
										<br>•	Have English notes and translation.
										<br>• The contents are structured to prepare students for JLPT since the questions are similar to the exam.
										<br>•	Many grammar examples to follow through that suit daily life.
										<br>• Consists of comprehensive and multiple choice questions at the end of each section to test your understanding.

              <p>
                  <b>Cons: </b>
									 <br>•	Not many opportunities to practice writing for the grammar sections since most of the questions are multiple-choice.

