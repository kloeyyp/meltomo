									<p><h2> 5. An Integrated Course in Elementary Japanese
									GENKI <br>
							      			by Eri Banno and various authors </h2>
									<p>
											 <h5> Summary:
												 <br>•	Comes in form of a textbook and a workbook with various versions for different levels of Japanese learners.
												 <br>•	Provides different sections including vocabulary, grammar, speaking practice and kanji into 12 lessons.
												 <br>•	A CD is provided for listening practice.
											 </h5>

								 <p>Average rating of 4.65 out of 5 on <a href="http://www.amazon.com/GENKI-Integrated-Elementary-Japanese-English/dp/4789014401/ref=sr_1_1?s=books&ie=UTF8&qid=1449426931&sr=1-1&keywords=genki+1">Amazon</a>.

									<p>		 <b>Pros: </b>
													  <br>•	Consists of English translation and notes.
														<br>•	Very insightful explanation on the grammar sections.
														<br>•	Contents are divided into 12 lessons, it allows the learner to find and refer to a specific topic more conveniently.
														<br>•	Genki also provides different online learning tools on their website like Kanji and Vocab application to help students to learn more effectively.
														<br>•	Students can find out more about GENKI self-learning apps and also access their online learning tools by clicking <a href="http://genki.japantimes.co.jp/self_en">here</a>.

									<p>
												<b>Cons: </b>
													 <br>•	Would be less effective if you study alone, as most of the practices in the book require a learning partner unless you skip the speaking practices!


