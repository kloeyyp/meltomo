							<p><h2> 3. Nihongo Sou Matome <br>
											Vocabulary by Matsumoto Kiko </h2>

	            <p>Rated as 4.3 out of 5 on <a href="http://www.amazon.com/Nihongo-Noryokushiken-Taisaku-matome-Vocabulary/dp/4872177312">Amazon</a> from 3 reviewers.

							<p>
								<h5> Summary: Book series that caters for N3 to N1 learners. </h5>
							<p>
									 <b>Pros: </b>
										<br>•	Easy to use for self-learner provides many useful words.
										<br>•	Organised the words in a specific theme so it makes it easier to learn.
										<br>• It comes with daily and weekly quiz and everything is structured similarly to JLPT exam questions.

							<p>
									<b>Cons: </b>
									<br>•	As it is a vocabulary book, some explanations may be a bit vague and require you to further look up the words in a dictionary or online.
									<br>•	Not many opportunities to practice writing so you may be able to recognize the Kanji after it being exposed to you in the book, but won’t be able to remember and write it.


