									<p><h2> 4. Minna no Nihongo Series <br>
													by Surie Nettowaku & Kabushiki Kaisha </h2>

									<p>
												 <h5> Summary: Book series that caters for beginners till higher intermediate level. </h5>

												 <p>Rated as 4 out of 5 on <a href="http://www.amazon.com/Minna-Nihongo-Book-Japanese-Edition/dp/4883191028">Amazon</a>.

									<p>		 <b>Pros: </b>
							      			  <br>•	Covers a range of vocabulary and gets harder as you progress on.
							      			  <br>•	Provides advance coverage of grammar.
									      	  <br>•	Has many practice exercises to keep you writing.
								      		  <br>•	Challenges you with harder exercises compared to many other textbooks.

									<p>
		   									<b>Cons: </b>
     	 										<br>•	A bit difficult to self study if you don’t have any background in learning Japanese at all (unless you purchase the other book that provides the English translations and grammar explanations).


