              <p><h2>1. Japanese Demystified <br>
                      by Eriko Sato </h2>

							<p>
								<h5> Summary: Basic Japanese book taught entirely in English.</h5>

							<p> Rated 4.2 out of 5 on <a href="http://www.amazon.com/Japanese-DeMYSTiFieD-Audio-2nd-Edition/dp/0071797718">Amazon</a>.

				      <p>
                   <b>Pro: </b>
									 <br>•	Extremely easy for beginners who want to self-study to use as explanations are quite detailed.

              <p>
                  <b>Cons: </b>
                   <br>•	Doesn’t cover much intermediate material, so you’d have to purchase other textbooks after finishing this one.
                   <br>•	The examples remain quite easy from the beginning to the end, so not much variety.


