							<h2> Shouko Kikuchi (20) <br>
菊地 祥子 </h2>
							<h5> Q1. 何でオーストラリア、そして何でメルボルンに来ましたか？</h5>
<p>厳しい家庭に生まれて、高校のときには両親に公務員になることを勧められたんですよ。それが嫌だったので、高２のときに家出して、ヒッチハイクをしたんですよね。そこでいろんな人に出会って、外国に行ってみればいいんじゃないと言ってもらったんです。沢山のワーキングホリデーの人がいると聞いてオーストラリアに決めました。メルボルンに決めたのは語学学校が充実しているからです！</p>

<h5>Q2. メルボルンの好きなところはどんなところですか？</h5>
<p>まずいろんなバックグラウンドを持つ人がいることですね。そしてカラフルな服を来ている人が多いのも魅力的ですよね〜笑　あと、自分は田舎出身なのでメルボルンのゆったりした感じにも引かれますね。シドニーはちょっと機械的すぎて。。。
</p>

