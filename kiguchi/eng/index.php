							<h5>  Q6. メルボルンを一言で言うと何だと思いますか？</h5>
<p>自分の実力が目に見える場所。</p>


<p>日本にいた時は自分の本当の気持ちを表現することが難しかったのですが、メルボルンでは逆に自分の気持ちを表現できないと相手にされません。だからこそ自分の考えをしっかりと述べ、努力し結果を残すことができれば周りに認められるようになるので、やりがいがあります。</p>











