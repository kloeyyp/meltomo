							<h5>Q3. オーストラリアでは主に何をしていますか？しょうこさんのメルボルンライフについて色々教えて下さい！笑</h5>
<p>とりあえずイケメンあさりと可愛い洋服探しと面白い人探しですね( 笑 )　まあ、主な仕事はイベントプロモーターですね。</p>
							<h5> Q4. メルボルンで何か面白いイベントに参加しましたか？</h5>
<p>そうですね。<a href="http://confest.org.au/">ConFest</a>は中々面白かったですね〜。オーストラリアの自然を体感できるような素敵なイベントで私のお気に入りでした。日本関連のイベントで言うと、「Japan Culture and Art Festival 日本文化芸術祭 2015」
	やコスプレイベントなどに参加しましたね。私がプロモートしたイベントと言えば、<a href="https://www.facebook.com/groups/thefifthcorner/?fref=ts">「The Fifth Corner Project」</a>　という団体が主催した「UNIナイト」ですね。このイベントは簡単に言えばメルボルンにいる日本人と日本に興味があるローカルの大学生の交流イベントです。その前身は去年開催された「Japanese Circle Goudou Party」なのですが、それが二回目より「UNIナイト」という名前に変わり、そこから私はプロモーターとして参加しました。「UNIナイト」以外にも、
	「U-DJ International Student Party」やメルボルンでの成人式イベントなどを企画開催しました。実は今度の「UNIナイト」は１１月１３日に開催されます。
サザンクロスに近いThe Bottom Endという場所で19:30より開催されます。詳細は<a href="https://www.facebook.com/events/531278493706667/">こちら</a>
</p>




