<h5> Q5.メルボルンでプロモーションやイベントに興味がある人にアドバイスをお願いします！</h5>
<p>とりあえず相手に印象を残す事が大切です。例えば、目立つような服や、ジョークを言ったりなど最初の三分が勝負です。沢山いる人の中から相手に覚えてもらうことで、それが自分のイベントのプロモーションにつながります。</p>

