							<h5>Konnichiwa everyone!</h5>
							<p>Recently, on the 13th of August, we held our first Meltomo nomikai at Melbourne University’s Tsubu Bar.</p>
							<p>‘What is a nomikai?’, you may be wondering. The word Nomikai (飲み会) means ‘drinking party’. In Japan, holding and attending drinking parties are very common parts people’s university and work culture, allowing them to deepen connections and de-stress. With university assignments to complete and the building tension at work, we thought it would be a great start to allow our users to get their loads off their shoulders and have some fun.</p>
							<p>With a turn out of more than 30 people, the night was deemed successful! A wide variety of delicious food and drinks could be purchased on site at Tsubu Bar. I personally had the Soy and Ginger Chicken, which was mouth-wateringly satisfying. The nomikai was highly interactive, the participants were seen introducing themselves and even communicating in their non-native languages. It definitely made my fellow team members happy, seeing so many smiles in that room the other day!</p>
							<p>We would like to thank every single one of you who came to our debut event. We hope you made lots of new friends, shared your interests and passion about each other’s culture, learnt new words in Japanese or English, and had enjoyable conversations! Also, a big thank you to Tsubu Bar for the delicious food and beer we gladly devoured and allowing us to hold our nomikai there!</p>
							<p>See you really soon at the next Meltomo event!<br><br> Meltomo Team.</p>

