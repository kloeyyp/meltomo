							<h2> Kazuma Onishi (29) <br>
大西 和馬 </h2>
<h3>"DJ CONi"</h3>
							<h5> Q1. メルボルンで何やってるんですか？</h5>
<p>DJとして活動しています。</p>

<h5>Q2. 何でオーストラリア、そして何でメルボルンに来ましたか？</h5>
<p>海外でDJがしたいと強く思うようになって、Working Holidayが使えるオーストラリアにしました。メルボルンはカフェ、アート、音楽の街、あと、世界で1番住みやすい街っていうのを知って決めました。</p>

