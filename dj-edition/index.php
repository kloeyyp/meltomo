							<h5>  Q6. オーストラリア又はメルボルンでのオススメ音楽フェスは何ですか？</h5>
<p>個人的には
<a href="http://www.earthcore.com.au">earthcore</a>, <a href="http://www.rainbowserpent.net">Rainbow Serpent Festival</a>。
ビッグフェスで注目してるのは、この２つですね。</p>

<h5> Q7. 次はどこでDJやりますか？</h5>
<p>基本的に毎週シティのクラブでDJ活動しています。
オススメ音楽フェスであげた<a href="http://www.earthcore.com.au/artists/kazuma-onishi/">earthcore</a>に今回インターナショナルDJとしてbooking頂きました。自分自身DJ人生の中で間違いなく1番大きい舞台になります。</p>

<h5> Q8. メルボルンでDJやりたいと思ってる人にアドバイスありますか？</h5>
<p>どこでやるとかは関係ないと思いますが、真剣にDJとして取り組むこと。言葉も通じない段階からDJとしてチームに入れてもらったのも、必死に熱意を伝え続けた結果かと、とにかく営業をがんばりましたね。笑
バイト探しのレジュメ配りと同じです。笑 </P>



							<h5>Q9. 最後に、メルボルンを一言で言うと何ですか？</h5>
<p>It's a small world って感じです。(Disney の)
自分にとっては、第2のhome。</p>



