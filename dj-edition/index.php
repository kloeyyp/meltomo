							<h5>Q3. 自分のメルボルンライフについて教えてください！笑</h5>
<p>スローライフですね、やりたいことをやりたいときに、みたいな感じで、ストレスのない毎日を過ごしたいように過ごしています。</p>
							<h5> Q4. どこでDJやっていますか？そして、おすすめのクラブはどこですか？</h5>
<p>所属している団体が２つあって１つは、この街のアジアンナイトクラブNo.1の<a href="http://www.tangevents.com.au">Tang</a>とうチーム。ここでは主に、シティのBONDというクラブで活動しています。もう１つは<a href="https://www.facebook.com/Versus.Melbourne">Versus</a>というチーム、イタリアンのオーガナイザー陣とtechno musicを中心にメルボルンの老舗、トップクラブのBrown ally や Royal Melbourne hotel 、夏にはボートを借りて300人規模のボートパーティをしています。</p>

<h5> Q5. 好きな音楽/アーティストは誰ですか？</h5>
<p><a href="https://en.m.wikipedia.org/wiki/Ken_Ishii">Ken Ishi</a>
DJをやりはじめたのも、この人を知ってからです。
Techno music 会のGod と呼ばれている日本人アーティストです。</p>

