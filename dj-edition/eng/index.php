							<h2> Kazuma Onishi (29) <br>
大西 和馬 </h2>
<h3>"DJ CONi"</h3>
							<h5> Q1. What are you doing here in Melbourne?</h5>
<p>I’m a DJ (DJ CONi).</p>

<h5>Q2. Why did you come to Australia? And why did you choose Melbourne?</h5>
<p> I really wanted to DJ overseas and I heard Australia was a popular place to come for working holidays. I also heard that Melbourne was famous for its cafe, art and music culture, as well as being voted the most liveable city in the world. Once I heard that, it was an easy decision.</p>

