							<h5>Q3. So tell us a little bit about your life here in Melbourne.</h5>
<p>I would say I have a pretty ‘slow’ stress-free lifestyle in Melbourne. I’m able to do what I want to do when I want to do it which is nice. I’ve been able to have a very Aussie everyday routine.</p>
							<h5> Q4. So where do you DJ? And do you have any clubs (apart from your own!) that you would recommend? </h5>
<p>The first team I’ve joined is Melbourne’s No.1 Asian event team <a href="http://www.tangevents.com.au">Tang</a>. We mainly perform at a night club in the CBD called ‘BOND’. The second team I’m involved with is called <a href="https://www.facebook.com/Versus.Melbourne">Versus</a> & I work with an organiser from Italy, focusing our style mainly on techno music. We rotate around some of Melbourne’s biggest clubs, including ‘Brown Ally’ and ‘Royal Melbourne Hotel’, and in the summer hold boat party events accommodating over 300 people. </p>



<h5> Q5. Who is your favourite artist?</h5>
<p><a href="https://en.m.wikipedia.org/wiki/Ken_Ishii">Ken Ishi</a>. He is actually the reason why I first got into DJ-ing. He’s a very popular Japanese artist, and is often called the ‘God of Techno Music’. He’s definitely had a huge influence on me.</p>

