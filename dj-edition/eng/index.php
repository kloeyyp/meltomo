							<h5>  Q6. Do you have any Australian (or Melbournian?) music festivals you can recommend?</h5>
<p>My personal favourites are
<a href="http://www.earthcore.com.au">earthcore</a> and <a href="http://www.rainbowserpent.net">Rainbow Serpent Festival</a>。
These two festivals are the main big festivals’ I would recommend.</p>

<h5> Q7. Where will you next be DJ-ing?</h5>
<p> I usually DJ every week at clubs in the CBD.
As for festivals, I have actually just received a booking to perform as an international DJ at <a href="http://www.earthcore.com.au/artists/kazuma-onishi/">earthcore</a>, one of the music festivals I mentioned above. It will be, without a doubt, one of the biggest performances of my career so far.</p>

<h5> Q8. Do you have any advice for people who are interested in getting involved in the DJ scene in Melbourne?</h5>
<p>As with anything, I believe it’s important to focus and work hard. I, myself, made sure I always expressed my passion for music and DJ-ing and always remembered to share with others my ambitions and goals for the future. I think that’s the reason I was able to get involved with the two teams I’m currently working with. I made sure my determination communicated through, regardless of the language barrier. In a sense, I guess it’s like handing out your resume when you’re job hunting! You can’t give up. </P>



							<h5>Q9. Finally, if you were to describe Melbourne in one word/phrase, what would it be?</h5>
<p> “It’s a small world” (yes, that’s a Disney reference!)
Melbourne has become my second home.</p>



