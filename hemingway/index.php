<p> 読み易さの下に表示されている文字数の”More”ボタンをクリックすると、段落数・文章数・単語数・英字数・英数字数・読むのに要する時間、といった詳しい情報が表示されます。

<p><u>ここでそれぞれの色付けの意味をまとめておきます:</u>

<br><br>黄色：読みにくい

<br><br>赤：とても読みにくい

<br><br>紫：他にもっとわかり易い言い回しがある言葉 (該当の言葉の上へカーソルを合わせると、提案された言い回しが表示されます。)

<br><br>青：他の副詞を使って表現できる言葉 (例えば、ただ”walking slowly”と打つのではなく、”tip-toed”や”crept”といった同じ意味を持つ他の副詞を使用することで、文章全体が表現豊かになり面白くなります。)

<br><br>緑：受動態 (エッセイの中で受動態を頻繁に使用しすぎると上品さに欠けます。なのでこの色が付いた部分は、いくつか能動態に変えるといいでしょう。)


<p>ヘミングウェイは、物書きさんにとってとても便利なツールですが、特に大量のエッセイを書かなければいけない学生さんにとっては、いちいち友達に添削を頼む手間も省け、さらに重宝するでしょう。

