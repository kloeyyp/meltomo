							<p>On this website, you can type in your essay right away and edit them by using the toolbar provided.
								 It will rate your readability, show your word count and also a clear display of what the editor thinks
								 you need to change to improve your essay.  When it grades your readability,
								 what it really means is the minimum education needed to understand your essay.
							<p>
                 If your text lights up like a Christmas tree, it means it requires quite a high-grade level to understand.
								 However, a higher grade means it is tedious for any readers. You do not want to sound like a textbook, do you?
								 Hemingway editor suggests to target a grade 10 readability as it is the average American standard.
