<p> If you click on the word count bar, it will provide you with exact and detailed information of the word count in your essay.

<p><u>Here is a summary of what the highlighted words mean:</u>

<br><br>Yellow: Hard to read.

<br><br>Red: Very hard to read.

<br><br>Purple: Phrases that have simpler alternatives (alternatives are suggested when you hover onto the phrase).

<br><br>Blue: Adverbs - instead of saying “walking slowly”, you can say “tip-toed” or “crept” to allow your writing to be more expressive and vivid.

<br><br>Green: Passive sentence – too much passive voice makes you sound less elegant in your essay. Thus, try to use less passive terms.


<p>Hemingway is a great tool for writers, and this is especially true to students if they want to save themselves from troubling their friends to check their written work all the time.

