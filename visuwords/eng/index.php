							<p>Visuwords is not difficult to use at all. On the search bar, you search a word you want by pressing <b>“enter”</b>.
								You can also opt for a random word by pressing the orange button beside the search bar.
							<p>
                 If you are curious and wish to know more about that particular word on the map, simply double-click on the word,
								 and a new map of words will spring out of it. Users can explore the diagram by dragging the map around as well as
								 zooming in and out of the diagram by rolling the wheel on their mouse. It is an unusual but a brand-new, modern and
								 interesting way to learn new words and concepts in an instant.
