              <p>Visuwords is an online graphical dictionary where it displays meanings, associations with other
								words and concepts into an elegant animated visual map based on a single word searched by a user.

							<p>This website is a great learning platform for all types of user, created by a team of students
								and language researchers from Princeton University in the United States.
