						<p>Too many words and don’t know where to start from? Just focus on the word that intrigues you the most and begin from
							that one. The problem surfaces only when you don’t know what you don’t know. With Visuwords, you can see how much you
							don’t know. Thus, choose a point and start from there. You can keep expanding the word into more maps.

