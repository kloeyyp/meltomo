              <p>Visuwordsは、ユーザーが一つの単語を打ち込むと、その単語に関連する他の単語やコンセプトをクモの巣
								グラフに表示してくれるオンラインのグラフィカル辞書です。

							<p>このサイトは、アメリカのプリンストン大学の学生と語学研究家らによって作られたもので、どんな人でも使える便利な勉強ツールです。
              <!-- article content 2 -->

