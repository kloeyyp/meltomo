						 <p>それでも物足りない！という人は、色々な単語をクリックし続けることで、
							 無限にその単語に関連するコンセプトと他の単語が飛び出してくるので、適当に単語をクリックしていくのも結構楽しいです。

						<p>もし単語が多すぎてどこから調べ始めたらいいかわからなければ、とりあえずどれでもいいから自分の目を引くものから始めていけばいいのです。
							個人的に、勉強に行き詰まった時のよくある原因 は「自分が何がわからないのかさえわからないこと」だと感じるのですが、
							Visuwordsだと自分がどれだけ知らないのかが一目で把握できるのです。なので、適当に開始点を作り、そこからどんどん新しいグラフを広げていっ
							て勉強すればいいのです。

