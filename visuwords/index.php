								<figure class="margin-bottom-20">
									<img class="img-responsive" src="../../assets/images/Visuwords/Visual2.png" alt="img" />
								</figure>


									<figure class="margin-bottom-20">
										<img class="img-responsive" src="../../assets/images/Visuwords/Visual3.png" alt="img" />
									</figure>

							<p>Visuwordsの使い方はとても簡単で、検索バーに調べたい単語を打ち込み <b>enter</b> をクリックするだけです。
								さらに、検索バーの横にあるオレンジのボタンをクリックすると無作為に選ばれた単語を表示してくれます。


							<p>
                 もしその単語についてもっと知りたいのであれば、単語をダブルクリックすると、そこからまた新しいクモの巣グラフが飛び出します。
								 表示されたグラフはマウスでドラッグしたり、マウスのスクロールを上下させることで拡大・縮小もでき、とてもインタラクティブな
								 ので興味の向くままにその単語について調べられます。すこし不思議に感じるかもしれませんが、Visuwordsは全く新しい、
								 モダンで面白い単語学習法です。
